# Matriz 2D (lista de listas)
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print("Matriz 2D:")
print(matrix)
print("Elemento en la fila 0, columna 1:", matrix[0][1])  # Accede al número 2

# Matriz 3D (lista de listas de listas)
matrix = [
    [[1, 2], [3, 4]],
    [[5, 6], [7, 8]]
]
print("\nMatriz 3D:")
print(matrix)
print("Elemento matrix[0][1][1]:", matrix[0][1][1])  # Accede al número 4

# Tupla (inmutable)
tupla = (1, 2, 3)
print("\nTupla:")
print("Tupla completa:", tupla)
print("Tipo de dato:", type(tupla))
print("Primer elemento de la tupla:", tupla[0])
print("Nota: Las tuplas son elementos inmutables")

