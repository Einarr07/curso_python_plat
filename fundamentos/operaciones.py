# Numerical operators
a = 8
b = 3
print("Suma:",a + b)
print("Resta:",a - b)
print("Mult:",a * b)
print("Division:",a / b)
print("Parte entera de la división: ", a // b)

# Module
print("Modulo:",a % b)
# Empowerment
print("Potenciación:",a ** b)

# Shortcuts
a += 2
print(f"Suma a más 2: {a}")

# Logical
print(a > b)
print(a >= b)
print(a < b)
print(a <= b)
print(a == b)
print(a != b)